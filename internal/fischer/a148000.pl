my %hash;
my ($i, $j, $k, $m);
my $start = time();
my $maxn = shift(@ARGV);

my $n = 0;
my $sum;
for ($m = 0; $m <= $maxn; $m ++) {
    $sum = 0;
    for ($i = 0; $i <= $m; $i ++) {
        for ($j = 0; $j <= $m; $j ++) {
            for ($k = 0; $k <= $m; $k ++) {
                $sum += &aux($i, $j, $k, $m);
            } # for $k
        } # for $j
    } # for $i
    print "$n $sum\n";
    $n ++;
} # for $m
my $elapsed = time - $start;
print "elapsed: $elapsed, #hash=" . scalar(%hash) . "\n";
#--------
sub aux {
    my ($i, $j, $k, $m) = @_;
    # my $key = sprintf("%02x%02x%02x%02x", $i,$j,$k,$m);
    my $key = "$i,$j,$k,$m";
    my $result;
    if (defined(  $hash{$key})) {
        $result = $hash{$key};
    } else {
        if (0) {
        } elsif (
                  $i > $m or $j > $m or $k > $m
                or   
                  $i < 0  or $j < 0  or $k < 0
                ) {
            $result = 0;
        } elsif ($m == 0) {
            if ($k == 0 and $j == 0 and $i == 0) {
                $result = 1; # the Kronecker delta
            } else {
                $result = 0;
            }
        } else {
            my $mn1 = $m - 1;         
            my $ip1 = $i + 1;         
            $result
            = &aux($i - 1, $j    , $k    , $mn1)
            + &aux($ip1  , $j - 1, $k    , $mn1)
            + &aux($ip1  , $j + 1, $k - 1, $mn1)
            + &aux($ip1  , $j + 1, $k + 1, $mn1)
            ;
        }
        $hash{$key} = $result;
    }
    # print "aux($i, $j, $k, $m) = $result\n";
    # $sum += $result;
    return $result;
} # aux
